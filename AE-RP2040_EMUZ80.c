    // SDK関数を介さず直接FIFO待ち＆読み込み
    while (pio0->fstat & (1u << (PIO_FSTAT_RXEMPTY_LSB + sm_emu))) {
    }
    uint32_t agpio = *rxfifo;
    uint32_t adrs_word = ((agpio >> 8) & 0x1FFF) | ((agpio >> 14) & 0xE000);
